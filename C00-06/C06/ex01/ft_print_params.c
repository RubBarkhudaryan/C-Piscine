#include <unistd.h>

int	main(int arg_c, char *arg_v[])
{
	int	i;
	int	j;

	if (arg_c >= 2)
	{
		i = 1;
		while (arg_v[i])
		{
			j = 0;
			while (arg_v[i][j])
			{
				write(1, &arg_v[i][j], 1);
				++j;
			}
			++i;
			write(1, "\n", 1);
		}
	}
	return (0);
}
