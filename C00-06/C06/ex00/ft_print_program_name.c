#include <unistd.h>

int	main(int arg_c, char *arg_v[])
{
	int	i;

	i = 0;
	if (arg_c)
	{
		while (arg_v[0][i])
		{
			write(1, &arg_v[0][i], 1);
			++i;
		}
	}
	write(1, "\n", 1);
	return (0);
}
