int	ft_is_prime(int n);

int	ft_is_prime(int n)
{
	int	i;

	i = 2;
	if (n < 2)
		return (0);
	while (i < n)
	{
		if (n % i == 0 && n != i)
			return (0);
		++i;
	}
	return (1);
}
